  name: 'BulkUploadVehicles',
  layout: 'dashboard',
  data() {
    return {
      selectedFile: null,
      isDragging: false,
      parsing: false,
      uploading: false,
      parsedData: [],
      uploadResult: null
    }
  },
  methods: {
    downloadTemplate() {
      // Create template data
      const templateData = [
        {
          '‡∏£‡∏∏‡πà‡∏ô‡∏£‡∏ñ': 'D-Max',
          '‡∏ó‡∏∞‡πÄ‡∏ö‡∏µ‡∏¢‡∏ô': '1‡∏Å‡∏Å 1234',
