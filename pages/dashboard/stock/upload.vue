        },
        {
          '‡∏£‡∏∏‡πà‡∏ô‡∏£‡∏ñ': 'MU-X',
          '‡∏ó‡∏∞‡πÄ‡∏ö‡∏µ‡∏¢‡∏ô': '2‡∏Å‡∏Ç 5678',
          '‡∏™‡∏µ': '‡πÄ‡∏á‡∏¥‡∏ô',
          '‡∏õ‡∏µ': 2024,
          '‡∏£‡∏≤‡∏Ñ‡∏≤': 1200000,
          '‡∏™‡∏ñ‡∏≤‡∏ô‡∏∞': 'available',
          '‡∏´‡∏°‡∏≤‡∏¢‡πÄ‡∏´‡∏ï‡∏∏': ''
        }
      ]

      // Create workbook and worksheet
      const ws = XLSX.utils.json_to_sheet(templateData)
      const wb = XLSX.utils.book_new()
      XLSX.utils.book_append_sheet(wb, ws, 'Vehicles')

      // Generate Excel file
      XLSX.writeFile(wb, 'vehicle_template.xlsx')
    },

    handleFileSelect(event) {
      const file = event.target.files[0]
      if (file) {
        this.selectedFile = file
      }
    },

    handleFileDrop(event) {
      this.isDragging = false
      const file = event.dataTransfer.files[0]
      if (file && (file.name.endsWith('.xlsx') || file.name.endsWith('.xls') || file.name.endsWith('.csv'))) {
        this.selectedFile = file
      } else {
        this.$toast?.error('‡∏Å‡∏£‡∏∏‡∏ì‡∏≤‡πÄ‡∏•‡∏∑‡∏≠‡∏Å‡πÑ‡∏ü‡∏•‡πå .xlsx, .xls ‡∏´‡∏£‡∏∑‡∏≠ .csv')
      }
    },

    async parseFile() {
      if (!this.selectedFile) return

      this.parsing = true

      try {
        const data = await this.readFile(this.selectedFile)
        const workbook = XLSX.read(data, { type: 'binary' })

        // Get first worksheet
        const firstSheetName = workbook.SheetNames[0]
        const worksheet = workbook.Sheets[firstSheetName]

        // Convert to JSON
        const jsonData = XLSX.utils.sheet_to_json(worksheet)

        // Map Thai column names to English
        this.parsedData = jsonData.map(row => ({
          model: row['‡∏£‡∏∏‡πà‡∏ô‡∏£‡∏ñ'] || row['model'],
          plateNumber: row['‡∏ó‡∏∞‡πÄ‡∏ö‡∏µ‡∏¢‡∏ô'] || row['plateNumber'] || row['plate_number'],
          color: row['‡∏™‡∏µ'] || row['color'],
          year: row['‡∏õ‡∏µ'] || row['year'],
          price: row['‡∏£‡∏≤‡∏Ñ‡∏≤'] || row['price'],
          status: row['‡∏™‡∏ñ‡∏≤‡∏ô‡∏∞'] || row['status'] || 'available',
          notes: row['‡∏´‡∏°‡∏≤‡∏¢‡πÄ‡∏´‡∏ï‡∏∏'] || row['notes'] || ''
        }))

        this.$toast?.success(`‡∏≠‡πà‡∏≤‡∏ô‡πÑ‡∏ü‡∏•‡πå‡∏™‡∏≥‡πÄ‡∏£‡πá‡∏à ‡∏û‡∏ö‡∏Ç‡πâ‡∏≠‡∏°‡∏π‡∏• ${this.parsedData.length} ‡∏£‡∏≤‡∏¢‡∏Å‡∏≤‡∏£`)
      } catch (error) {
        console.error('Error parsing file:', error)
        this.$toast?.error('‡πÑ‡∏°‡πà‡∏™‡∏≤‡∏°‡∏≤‡∏£‡∏ñ‡∏≠‡πà‡∏≤‡∏ô‡πÑ‡∏ü‡∏•‡πå‡πÑ‡∏î‡πâ')
      } finally {
        this.parsing = false
      }
    },

    readFile(file) {
      return new Promise((resolve, reject) => {
        const reader = new FileReader()
        reader.onload = (e) => resolve(e.target.result)
        reader.onerror = reject
        reader.readAsBinaryString(file)
      })
    },

    async uploadData() {
      if (this.parsedData.length === 0) return

      this.uploading = true
      let imported = 0
      let failed = 0
      const errors = []

      try {
        console.log('üì§ Starting bulk upload of', this.parsedData.length, 'vehicles')

        // Upload vehicles one by one since bulk endpoint doesn't exist
        for (let i = 0; i < this.parsedData.length; i++) {
          const row = this.parsedData[i]

          try {
            const vehicleData = {
              model: row.model,
              plate_number: row.plateNumber,
              color: row.color || null,
              year: row.year || null,
              price: row.price || null,
              status: row.status || 'available',
              notes: row.notes || null
            }

            console.log(`üì§ Uploading vehicle ${i + 1}/${this.parsedData.length}:`, vehicleData)

            // Use correct API endpoint: POST /api/stock
            await this.$api.stock.create(vehicleData)
            imported++
          } catch (error) {
            failed++
            const errorMsg = error.response?.data?.message || error.message
            errors.push(`‡πÅ‡∏ñ‡∏ß‡∏ó‡∏µ‡πà ${i + 1}: ${errorMsg}`)
            console.error(`‚ùå Failed to upload vehicle ${i + 1}:`, error.response?.data || error.message)
          }
        }

        this.uploadResult = { imported, failed }

        if (imported > 0) {
          this.$toast?.success(`‡∏ô‡∏≥‡πÄ‡∏Ç‡πâ‡∏≤‡∏Ç‡πâ‡∏≠‡∏°‡∏π‡∏•‡∏™‡∏≥‡πÄ‡∏£‡πá‡∏à ${imported} ‡∏£‡∏≤‡∏¢‡∏Å‡∏≤‡∏£`)
        }

        if (failed > 0) {
          console.warn('‚ùå Upload errors:', errors)
          this.$toast?.error(`‡∏≠‡∏±‡∏û‡πÇ‡∏´‡∏•‡∏î‡∏•‡πâ‡∏°‡πÄ‡∏´‡∏•‡∏ß ${failed} ‡∏£‡∏≤‡∏¢‡∏Å‡∏≤‡∏£ - ‡∏ï‡∏£‡∏ß‡∏à‡∏™‡∏≠‡∏ö console`)
        }
      } catch (error) {
        console.error('‚ùå Error uploading vehicles:', error)

        const errorMessage = error.response?.data?.message
          || error.response?.data?.error
          || error.message
          || '‡πÑ‡∏°‡πà‡∏™‡∏≤‡∏°‡∏≤‡∏£‡∏ñ‡∏≠‡∏±‡∏û‡πÇ‡∏´‡∏•‡∏î‡∏Ç‡πâ‡∏≠‡∏°‡∏π‡∏•‡πÑ‡∏î‡πâ'

        this.$toast?.error(`‡πÄ‡∏Å‡∏¥‡∏î‡∏Ç‡πâ‡∏≠‡∏ú‡∏¥‡∏î‡∏û‡∏•‡∏≤‡∏î: ${errorMessage}`)
      } finally {
        this.uploading = false
      }
    },

    clearData() {
      this.parsedData = []
      this.selectedFile = null
      this.$refs.fileInput.value = ''
    },

    resetForm() {
      this.selectedFile = null
      this.parsedData = []
      this.uploadResult = null
      this.$refs.fileInput.value = ''
    }
  }
}
</script>
